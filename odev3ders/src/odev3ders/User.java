package odev3ders;

public class User {
	int id;
	String firstName;
	String lastName;
	String mail;
	String password;
	
	
	public User (int id,String firstName,String lastName,String mail,String password) {
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.mail = mail;
		this.password = password;
	}
}
